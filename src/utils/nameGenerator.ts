export function generateUsername(): string {
  const adj1 = adjectives[Math.floor(Math.random() * adjectives.length)];
  let adj2: string;
  do {
    adj2 = adjectives[Math.floor(Math.random() * adjectives.length)];
  } while (adj2 === adj1); // Ensure different adjectives

  const noun = nouns[Math.floor(Math.random() * nouns.length)];

  return `${adj1}-${adj2}-${noun}`;
}

const LOCAL_STORAGE_KEY = "lean-coffee-username";

export function getStoredUsername(): string | null {
  return localStorage.getItem(LOCAL_STORAGE_KEY);
}

export function storeUsername(username: string): void {
  localStorage.setItem(LOCAL_STORAGE_KEY, username);
}
